package day7;

public class Calc {

	public static int add(int a, int b){
		// a+b;
		// return; -> void
		return a+b;
	}
	
	public static int sub(int a, int b){
		return a-b;
	}
	public static int multi(int a, int b){
		return a*b;
	}
	public static int div(int a, int b){
		return a/b;
	}
}
